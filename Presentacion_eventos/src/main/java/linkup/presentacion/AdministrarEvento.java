    public AdministrarEvento(EventoDTO eventoDTO, ControlCrearEvento controlEvento, List<EventoDTO> eventos) {
        this.eventoDTO = eventoDTO;
        this.controlEvento = controlEvento;
        this.eventos = eventos;
        initComponents();
        setLocationRelativeTo(null);

        JTextField[] campos = {
            jTextField1, jTextField2, jTextField3,
            jTextField4, jTextField5, jTextField6
        };

        for (JTextField campo : campos) {
            campo.setOpaque(false);
            campo.setBackground(new Color(0, 0, 0, 0));
            campo.setBorder(null);
            campo.setForeground(Color.BLACK);
            campo.setEditable(false);
        }

        jComboBoxEtiqueta.setSelectedItem(eventoDTO.getEtiqueta().getTexto());
        jComboBoxEtiqueta.setEnabled(false);

        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        String fechaTexto = eventoDTO.getFechaHora().format(formatter);

        DateTimeFormatter formatter2 = DateTimeFormatter.ofPattern("HH:mm");
        String horaTexto = eventoDTO.getFechaHora().format(formatter2);

        jTextField4.setText(horaTexto);

        jTextField4.setText(fechaTexto);

        jTextField1.setText(eventoDTO.getNombreEvento());
        jTextField2.setText(eventoDTO.getEtiqueta().getTexto());
        jTextField3.setText(eventoDTO.getDescripcion());
        jTextField4.setText(fechaTexto);
        jTextField5.setText(horaTexto);
        jTextField6.setText(eventoDTO.getDireccion());
    }

